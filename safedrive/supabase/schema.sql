-- ────────────────────────────────────────────────────────────
-- 7. HELPER FUNCTION: generate_policy_number()
-- ────────────────────────────────────────────────────────────
create or replace function public.generate_policy_number()
returns text language plpgsql as $$
declare
  chars  text    := 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
  result text    := '';
  i      integer := 0;
begin
  for i in 1..6 loop
    result := result || substr(chars, floor(random() * length(chars) + 1)::int, 1);
  end loop;
  return 'SD-' || to_char(now(), 'YYYY') || '-' || result;
end;
$$;

-- ────────────────────────────────────────────────────────────
-- 8. HELPER FUNCTION: generate_claim_number()
-- ────────────────────────────────────────────────────────────
create or replace function public.generate_claim_number()
returns text language plpgsql as $$
declare
  result text := '';
  i      integer := 0;
  chars  text := '0123456789ABCDEFGHJKLMNPQRSTUVWXYZ';
begin
  for i in 1..7 loop
    result := result || substr(chars, floor(random() * length(chars) + 1)::int, 1);
  end loop;
  return 'CLM-' || to_char(now(), 'YYYY') || '-' || result;
end;
$$;
